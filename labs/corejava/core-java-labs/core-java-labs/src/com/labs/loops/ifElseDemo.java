package com.labs.loops;

public class ifElseDemo {

	public static void main(String[] args) {

		int score = 93;

		if (score > 90) {
			System.out.println("Grade is A");
		} else {
			System.out.println("Grade is B");

		}
		if (score > 90)
			System.out.println("Grade is A");
		else
			System.out.println("Grade is B");

	}
}

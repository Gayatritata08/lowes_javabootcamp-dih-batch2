package com.labs.loops;

public class switchDemo2 {

	public static void main(String[] args) {
		String number = "Three";

		switch (number) {

		case "one":
			System.out.println("1");
			break;
		case "Two":
			System.out.println("2");
			break;
		case "Three":
			System.out.println("3");
			break;

		case "Four":
			System.out.println("4");
			break;

		default:
			System.out.println("not in the list");

		}
	}

}
